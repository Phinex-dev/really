function sa(t) {
  const s = Date.now(),
    n = 1440 * 60 * 1e3,
    a = new Date();
  a.setHours(0, 0, 0, 0);
  const o = a.getTime();
  if (t >= o) return "今天";
  if (t >= o - n) return "昨天";
  const l = Math.floor((s - t) / n);
  if (l < 7) return `${l} 天前`;
  const c = new Date(t);
  return `${c.getMonth() + 1}月${c.getDate()}日`;
}
function na(t) {
  const s = new Date(t),
    n = (s.getMonth() + 1).toString().padStart(2, "0"),
    a = s.getDate().toString().padStart(2, "0"),
    o = s.getHours().toString().padStart(2, "0"),
    l = s.getMinutes().toString().padStart(2, "0");
  return `${n}-${a} ${o}:${l}`;
}
function aa(t, s) {
  return t ? (t.length > s ? t.slice(0, s) + "…" : t) : "";
}
